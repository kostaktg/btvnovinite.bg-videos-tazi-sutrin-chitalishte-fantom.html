				<h2 id="C1" style="padding-top:50px;">ESTABLISHMENT AND MEMBERSHIP</h2>
				<h3 style="padding-top:20px;">National Library “The National Library - Virtual world” -Blagoevgrad</h3>
				<p>
					<ul>

						<li>Чл.12.National Library <strong>“The National Library - Virtual world” -Blagoevgrad</strong> Was set up by 111 (one hundred and eleven) able-bodied individuals at the Constituent General Assembly</li>

						<li>Чл.13.
The Constituent Assembly adopted the Charter of The National Library and elected its organs</li>

						<li>Чл.14.The statutes determine:<ul><li>
The name</li>
						<li>Seat</li>
						<li>Objectives</li>
						<li>
Sources of funding</li>
					</ul>
					
The management and control authorities, their powers, the way they are chosen and decisions.
					The way of accepting members and the termination of membership, as well as the procedure for determining the membership fee.
				</li>

				<li>Чл.15. 
The National Library can find branches in the neighborhoods and residential areas of Blagoevgrad and the villages of Blagoevgrad Municipality.</li>

				<li>Чл.16.(1)
The National Library can find branches in the neighborhoods and residential areas of Blagoevgrad and the villages of Blagoevgrad Municipality.

					<ul>
						<li>(2)
The registration of the The National Library in the Register of the Regional Court shall be made upon written request by The National Library Chairperson, to which shall be attached:
							<ul>
								<li>The Protocol of the Constituent Assembly.</li>
								<li>The The National Library's constitution, signed by the founders.
								</li>
								<li>Notary certified by the signature of the Chairperson of the The National Library Board of Trustees, who represents it and the valid seal of the The National Library.
								</li>
							</ul>
						</li>
						<li>(3) 
The amendment to the Articles of Association provided for in the The National Library Act Act and the name of the The National Library Chairperson are subject to amendment.
						</li>
						<li>(4)
Any change in the circumstances under para. 3 shall be filed within 14 days of the occurrence for registration in the Register of Blagoevgrad Regional Court by the The National Library Chairperson according to Art. 9, para. 3 of the The National Library Act.
						</li>
					</ul>
				</li>
				<h3 id="C2" style="padding-top:50px">
Membership rights and obligations</h3>
				<li>Чл.17. The National Library membership is voluntary.

				</li>

				<li>Чл.18. The National Library members are individual, collective and honorary.

				</li>

				<li>Чл.18. The National Library members are individual, collective and honorary.
					<ul>
						<li>(1) The individual members are Bulgarian citizens. They are real and auxiliary:
							<ul><li>The actual members are capable persons, pay regularly the membership fee and have the right to vote;
The auxiliary members are persons under 18 years of age, have no right to vote and to be elected in The National Library Board and have a consultative voice.</li></ul></li>

								<li>(2) Collective members contribute to the achievement of the The National Library objectives, support the activities, maintain and enrich the facilities and have the right to one vote. Collective members may be:
									<ul><li>Professional organizations
									</li>
									<li>Business organizations
									</li>
									<li>Commercial companies
									</li>
									<li>Cooperatives and associations
									</li>
									<li>Cultural-educational and amateur clubs and creative collectives.
									</li>
								</ul>
								<li>(3)
Honorary members can be Bulgarian and foreign citizens with exceptional merits for The National Library
								</li>
							</li>
						</ul>
					</li>

					<li>Чл.19. Each member of the The National Library is entitled:
						<ul><li>To participate in the activity of the The National Library and in the work of the General Assembly
						</li>
						<li>To be elected in its governing bodies
						</li>
						<li>To control the work of the The National Library and the governing bodies
						</li>
						<li>
To be informed about the activities of the The National Library
						</li>
						<li> To use the property and the results of the The National Library activities free of charge
						</li>
					</ul>
				</li>

				<li>Чл.20. Each member of the The National Library is obliged:
					<ul>
						<li>
							
To import monthly membership dues by the 10th of the month
						</li>
						<li>
							
To observe the Charter of the The National Library and to work towards the achievement of its goals
						</li>
						<li>
							To work to increase the The National Library's assets and to increase its public authority
						</li>
					</ul>
				</li>

				<li>Чл.21.
Membership rights and obligations are non-transferable and do not pass on to others in the event of death or termination of membership. The exercise of membership rights may be granted to another person by authorization with a power of attorney by notary certification of the signature
				</li>
				<h3 id="C3" style="padding-top:50px">
Acquisition of membership</h3>

				<li>Чл.22. 
The members of the The National Library are accepted by the The National Library Chairperson on the basis of their written request.

				</li>

				<h3  style="padding-top:20px;">
Termination of membership</h3>

				<li>Чл.23.(1) The National Library membership ceases:
					<ul>
						<li>
With a unilateral statement to the The National Library Board of Trustees
						<li>With the death or placement under full disability
						</li>
						<li>
With exclusion
						</li>
						<li>With the end of the The National Library
						</li>
						<li>When dropping out
						</li>
					</ul></li>

					<li>(2) 
Disappearance of membership occurs when there is a violation of Article 20 of these Statutes. The violation is found by the The National Library Secretary, who makes a motivated proposal to the President, to exclude individual members
					</li>

					<li>(3) 
The decision to exclude is taken by the Chairperson of the The National Library in the presence of guilty behavior under Article 20 of this Statute, which makes the further membership in the The National Library incompatible. The decision can be appealed to the The National Library General Assembly within 14 days
					</li>
					<h2 id="C5" style="padding-top:50px">MANAGEMENT</h2>
					<h3  style="padding-top:20px;">The National Library management bodies</h3>

					<li>Чл.24. The The National Library's governing bodies are:
						<ul>
							<li>General Assembly
							</li>
							<li>
Board of Trustees
							</li>
							<li>
The Verification Commission.
							</li>
						</ul></li>
						<h2 id="C4" style="padding-top:50px" >GENERAL ASSEMBLY</h2>
						<h3  style="padding-top:20px;">Composition of the General Assembly</h3>
						<li>Чл.25. The The National Library's supreme body is the General Assembly and consists of:
							<ul>
								<li>
									
The actual members who regularly pay the membership fee and have the right to vote in the The National Library
								</li>
								<li>The collective members who are entitled to one vote
								</li>
							</ul></li>

							<li>Чл.26. 
The General Assembly of the The National Library consists of all members of the The National Library, who have the right to vote and they can not be less than 100 according to Article 8, paragraph 1 of the The National Library Act
							</li>

							<h3  style="padding-top:20px;">
Representation</h3>
							<li>Чл.27.(1) 
Members - legal entities are represented in the General Meeting by their legal representatives or an explicitly authorized person
								<ul>
									<li>(2) A proxy of a legal or natural person may be an individual only
									</li>
									<li>(3) The proxies are expressly issued for participation in the General Assembly of the The National Library and can be issued for limited or unlimited number of sessions of the General Assembly
									</li>
									<li>(4) Proxies do not have the right to re-authorize third parties
									</li>
									<li>(5) Plenipotentiaries may represent only one member of the General Assembly
									</li>
								</ul>
							</li>
							<h3 style="padding-top:20px;"> Competence of the General Assembly</h3>

							<li>Чл.28.(1) General Assembly:
							<ul><li><ul>
									<li>Amends the Statutes
									</li>
									<li>
Elects and dismisses the members of the Board of Trustees, the Audit Committee Chairman
									</li>
									<li>
Adopts the internal acts necessary for the organization of the The National Library activity
									</li>
									<li>Excludes members of the The National Library
									</li>
									<li>
Assigns to the Chairperson to accept members of the The National Library, to restrict their rights in the The National Library and to exclude members of the The National Library according to the interests of the The National Library in violation of Article 20 thereof
									</li>
									<li>
Adopts basic guidelines for the activities of the The National Library
									</li>
									<li>Decides to join or terminate membership in a The National Library Union
									</li>
									<li>Adopts the budget of the The National Library
									</li>
									<li>Adopt the annual report
									</li>
									<li>Affirms the monthly membership fees of the partners
									</li>
									<li>Assigns to the Board of Trustees to determine the monthly membership fees of the partners according to the needs of the company
									</li>
									<li>Revokes the decisions of the The National Library authorities
									</li>
									<li>Decides on the opening of branches of the The National Library after coordination with the municipality
									</li>
									<li>Decides to terminate the The National Library
									</li>
									<li>Takes a decision to the court of unlawful actions of the management or individual The National Library members
									</li>
								</ul></li>
							</li>

							<li>(2) The decisions of the General Assembly are obligatory for the other The National Library bodies

							</li>

							<li>(3) The decisions of the General Meeting are subject to judicial review of their legality and compliance with the Statutes

							</li>

							<li>(4) The decisions of the The National Library authorities, taken in contravention of the law, the statute or the transitional decision of the General Assembly, may be disputed before the General Assembly at the request of the members of the The National Library or of his or her authority, within one month of their knowledge; Not later than one year from the date of the decision

							</li>

							<li>(5)Disputes under paragraph 4 may be brought before the court of registration of the The National Library by any member or member of its body or by the prosecutor within one month of becoming aware of it but not later than one year from the date of the decision

							</li></ul>
							<h3  style="padding-top:20px;">Convening, conducting and quorum of the General Assembly</h3>
							<ul>
							<li>Чл.29.(1) A joint General meeting of the The National Library is convened by the Board of Trustees at least once a year

							</li>
							<li>(2) An Extraordinary General Meeting may be convened by decision of the Board of Trustees at the request of the Audit Committee or one third of the members of the The National Library

							</li>
							<li>(3) The invitation to the meeting must contain the agenda, the date, the time and place of the meeting, and who summoned it. It must be received no later than 7 days before the date of the event. At the same time, an announcement of the meeting must also be announced in public places

							</li>
							<li>(4) The General Meeting is lawful if at least half of the members of the The National Library have the right to vote. In the absence of a quorum, the meeting is scheduled for another date not earlier than one week. Then the assembly is legitimate, as members are present

							</li>
							<li>(5) The decisions under Article 28, para. 1, items 1, 4, 12, 13 and 14 shall be taken by a majority of at least two thirds of all members. The other decisions are taken by a majority of more than half of the members present.

							</li>
							<h3  id="C6" style="padding-top:50px">List of attendees</h3>

							<li>Чл.30.(1) A list of attending members or their representatives shall be drawn up at the meeting of the General Assembly, which shall certify their presence by signature and shall be legitimised. The list shall be endorsed by the Chairman of the General Assembly

</li>

							<li>(2) The list under the preceding paragraph includes members who have stated for their presence until the time of the first vote after the establishment of a quorum

</li>
<h3  style="padding-top:20px;">Conflict of interests</h3>
							<li>Чл.31. A member of the The National Library or his representative may not vote in:
							<ul>
								<li>
									
Lodge claims against him
								</li>
								<li>Taking action or refusing to act for the responsibility of the The National Library
</li>
								<li>When dealing with matters pertaining to him, his wife or relatives in a straight line - without limit, by collateral line - up to fourth degree or by marriage - up to second degree inclusive.
</li>
							</ul>

<h3>Solutions</h3>
							<li>Чл.32.(1) 
The General Assembly can not take decisions affecting issues that have not been published in the invitation, except where none of the members present or represented at the meeting no longer object that the issues raised should be discussed
</li>

							<li>(2) 
Decisions of the General Assembly shall enter into force immediately unless their action is postponed or, by law, enter into force after promulgation.

</li>

<h3>Протокол</h3>

							<li>Чл.33.(1) 
The minutes of the General Assembly are kept in a special book. The record is kept as required by law

</li>
<ul>

							<li>(2) The minutes of the General Assembly shall be signed by the chairman and the secretary of the meeting, and by the counts of the votes. A list of the attendants and the documents related to the convening of the General Meeting shall be attached to the minutes

</li>


							<li>(3)Every member present at the General Assembly has the right to request and monitor the accurate recording of the decisions in the minutes.
</li>

							</ul>
						</ul></p>
						
